def calc_subindex(pollutant, value):
    for bp_low, bp_high, index_low, index_high in breakpoints[pollutant]:
        if bp_low <= value <= bp_high:
            aqi= ((index_high - index_low) / (bp_high - bp_low)) * (value - bp_low) + index_low
            print("val",aqi)
            return aqi
    return None

